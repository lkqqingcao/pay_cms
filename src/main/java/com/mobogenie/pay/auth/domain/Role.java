package com.mobogenie.pay.auth.domain;

import java.util.List;

/**
 * 
 * @author wanghongfeng
 * @date 2014年7月12日
 */
public class Role {
	private Integer id;
	private String name;
	private String description;
	private Boolean hasit;
	private List<Resource> rslist;
	
	public Boolean getHasit() {
		return hasit;
	}
	public void setHasit(Boolean hasit) {
		this.hasit = hasit;
	}
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public List<Resource> getRslist() {
		return rslist;
	}
	public void setRslist(List<Resource> rslist) {
		this.rslist = rslist;
	}
	@Override
    public String toString() {
	    return "Role [id=" + id + ", name=" + name + ", description=" + description + ", hasit="
	            + hasit + ", rslist=" + rslist + "]";
    }
	

}
